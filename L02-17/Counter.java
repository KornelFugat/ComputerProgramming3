public class Counter{
    int value = 0;
    
    public void plusOne(){
        value += 1;
    }
    public void minusOne(){
        value -= 1;
    }
    public void plusTen(){
        value += 10;
    }
    public void minusTen(){
        value -= 10;
    }
    public void reset(){
        value = 0;
    }
}
